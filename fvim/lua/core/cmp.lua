local M = {}


local status_cmp_ok, cmp = pcall(require, "cmp")
  if not status_cmp_ok then
    return
  end

  local is_emmet_active = function()
  local clients = vim.lsp.buf_get_clients()

  for _, client in pairs(clients) do
    if client.name == "emmet_ls" then
      return true
    end
  end
  return false
end

